// Sử dụng Math.abs() - trả về giá trị tuyệt đối của số
let abs1 = Math.abs(-5.5);
console.log("Math.abs(-5.5) = " + abs1); // Kết quả sẽ là 5.5, vì giá trị tuyệt đối của -5.5 là 5.5

let abs2 = Math.abs(10);
console.log("Math.abs(10) = " + abs2); // Kết quả sẽ là 10, vì giá trị tuyệt đối của 10 là 10

// Sử dụng Math.ceil() - làm tròn lên số nguyên gần nhất
let ceil1 = Math.ceil(4.4);
console.log("Math.ceil(4.4) = " + ceil1); // Kết quả sẽ là 5, vì Math.ceil làm tròn số lên gần nhất

let ceil2 = Math.ceil(5.8);
console.log("Math.ceil(5.8) = " + ceil2); // Kết quả sẽ là 6, vì Math.ceil làm tròn số lên gần nhất

// Sử dụng Math.floor() - làm tròn xuống số nguyên gần nhất
let floor1 = Math.floor(5.7);
console.log("Math.floor(5.7) = " + floor1); // Kết quả sẽ là 5, vì Math.floor làm tròn số xuống gần nhất

let floor2 = Math.floor(6.1);
console.log("Math.floor(6.1) = " + floor2); // Kết quả sẽ là 6, vì Math.floor làm tròn số xuống gần nhất

// Sử dụng Math.round() - làm tròn số đến số nguyên gần nhất
let round1 = Math.round(10.1);
console.log("Math.round(10.1) = " + round1); // Kết quả sẽ là 10, vì Math.round làm tròn số gần nhất

let round2 = Math.round(20.6);
console.log("Math.round(20.6) = " + round2); // Kết quả sẽ là 21, vì Math.round làm tròn số gần nhất
